package opreator;

//目标：关系运算符（条件运算符）
public class demo_6 {
    public static void main(String[] args) {
    int a = 10, b = 9 , c = 10;
    System.out.println(a == b);
    System.out.println(a == c);
    System.out.println(a != b);
    System.out.println(a > b);
    System.out.println(a <= c);
    System.out.println(a < c);
    }
}
